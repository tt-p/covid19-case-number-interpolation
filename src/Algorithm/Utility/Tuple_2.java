package Algorithm.Utility;

public class Tuple_2 {

    private double x;
    private double y;

    public Tuple_2(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    @Override
    public String toString() {
        return "(" + x + ", " + y + ")";
    }
}
